while True:
    reply = input("Enter Text: ")
    if reply == 'Hello':
        print("Hi Buddy...😍")
    elif reply == 'Hi':
        print("Hello Buddy...🤣")
    elif reply == 'Bye':
        print("See you soon...😂")
    elif reply == 'Good':
        print("Great...👌")
    elif reply == 'stop':
        print("BYE BYE...😄")
        break
